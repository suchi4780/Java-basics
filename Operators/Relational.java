package Operators;

public class Relational {
    public static void main(String[] args) {
        int a = 20;
        int b = 10;
         System.out.println("sum = "+ (a>b) );
         System.out.println("sum = "+ (a<b) );
         System.out.println("sum = "+ (a>=b) );
         System.out.println("sum = "+ (a<=b) );
         System.out.println("sum = "+ (a==b) );
         System.out.println("sum = "+ (a!=b) );
    }
}
